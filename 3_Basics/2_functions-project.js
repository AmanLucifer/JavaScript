

//Additions
function add(num1 , num2){
    return num1 + num2

}

//Subratiton
function sub(num1 , num2){
    return num1 - num2

}


//Multiplication 
function multilply(num1 , num2){
    return num1 * num2

}
//Division 
function divide(num1 , num2){
    return num1 / num2

}


console.log(add(5,7))  //output ==> 12
console.log(sub(5 ,7))  //output ==> -2
console.log(multilply (5 ,7))  //output ==> 35
console.log(divide(10,5))  //output ==> 2